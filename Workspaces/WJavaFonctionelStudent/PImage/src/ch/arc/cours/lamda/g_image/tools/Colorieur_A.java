
package ch.arc.cours.lamda.g_image.tools;

public abstract class Colorieur_A
	{

	/*------------------------------------------------------------------*\
	|*							Constructeurs							*|
	\*------------------------------------------------------------------*/

	public Colorieur_A(int w, int h)
		{
		this.w = w;
		this.h = h;
		}

	/*------------------------------------------------------------------*\
	|*							Methodes abstract						*|
	\*------------------------------------------------------------------*/

	public abstract int colorIJ(int i, int j);

	/*------------------------------------------------------------------*\
	|*							Methodes Public							*|
	\*------------------------------------------------------------------*/

	public int color(int s)
		{
		int i = s / w;
		int j = s % w;

		return colorIJ(i, j);
		}

	/*------------------------------------------------------------------*\
	|*							Methodes Private						*|
	\*------------------------------------------------------------------*/


	/*------------------------------------------------------------------*\
	|*							Attributs Private						*|
	\*------------------------------------------------------------------*/

	// Inputs
	protected final int w;
	protected final int h;



	}
